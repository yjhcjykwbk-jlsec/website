            	<p>
	南燕律师事务所（“南燕”）成立于1992年，是中国成立最早、规模最大的综合性律师事务所。</p>
<p>
	南燕自2004年起进行了从个体化作业向团队化作业转型，向规模化、规范化、专业化、品牌化、国际化方向发展的探索和改革。通过一系列的研究和改革，南燕目前已实现了跨越式发展，不仅规模化建设取得明显成效，而且规范化、专业化、团队化建设也取得了丰硕成果。南燕现已建立了先进的管理体制和专业化团队作业模式，奠定了有效控制业务质量的基础，建设了资源共享机制的路径。南燕将坚持不懈地积极探索中国律师事务所品牌化、国际化的建设和发展道路。</p>
<p>
	南燕拥有3500余名律师及其他专业人员。其中，大多数律师毕业于国内外知名的法学院校；多数律师取得了美、英、法、日、韩、俄等国一流法学院校学位，并具有在国际知名律师事务所工作的经验；相当数量的律师还具备国际贸易、金融、建筑工程、工商管理、会计、税务等其他专业背景。南燕的律师及其他专业人员能够以英、法、日、韩、俄、蒙古等语言为客户提供服务。</p>
<p>
	南燕作为一家大型的、综合性的律师事务所，始终贯彻最大程度维护客户利益的执业宗旨，为国内外客户及时提供专业的、全面的、务实的法律及商务解决方案。南燕的主要专业领域包括：公司综合类业务、公司收购、兼并与重组、证券与资本市场、私募股权与投资基金、国企改制与产权交易、银行与金融、外商直接投资与外资并购、境外投资、反垄断与国家安全审查、税务、国际贸易、国际贸易救济与WTO业务、海商海事、知识产权、房地产与建设工程、矿业、能源与自然资源、诉讼仲裁、刑事辩护和劳动法等。</p>
<p>
	南燕已建立了覆盖全国、遍布世界重要城市的全球法律服务网络，南燕的境内外机构均可共享南燕全球法律服务网络内的项目信息、专业知识、业务经验、专业人才、社会关系等资源。南燕的总部设在北京，在长春、长沙、常州、成都、重庆、福州、广州、哈尔滨、海口、杭州、合肥、呼和浩特、黄石、吉林、济南、昆明、南昌、南京、南宁、南通、宁波、青岛、苏州、上海、深圳、沈阳、天津、太原、武汉、无锡、乌鲁木齐、温州、西宁、厦门、西安、银川、郑州、舟山、珠海等39个境内城市均设有分所。同时，南燕在芝加哥、巴黎、香港、洛杉矶、纽约、新加坡、台湾、莫斯科及蒙古等境外均设有当地律师事务所。</p>
<p>
	南燕作为中国区唯一成员，加入了世界最大的、汇集全球顶级律师事务所、会计师事务所、投资机构、金融机构等专业性服务公司的独立专业服务组织World Service Group（“WSG”世界服务集团）。南燕与100多个国家的律师事务所、会计师事务所、投资机构、金融机构的8万多名专业人士建立起了长期稳定的信息交换渠道和业务合作平台，更有效地满足国内外客户的全球化的法律及商务服务需求。</p>

            
